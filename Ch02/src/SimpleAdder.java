
public class SimpleAdder {
	public static void main(String[] args) {
		//변수를 사용하기위해서는 먼저 선언하고
		//변수에 초기값을 대입해야합니다.
		int num;//선언문- int타입의 변수 num을 선언하라는 명령문;
		num=10+20;//대입문 - 오른쪽의 10과20을 더한 결과값을 왼쪽의 num에 대입하라는 명령문;
		System.out.println(num);//System클래스의 out변수에 속하는 println()메소드를
		                        //호출하여 num값을 출력하라는 명령문;

	}

}
